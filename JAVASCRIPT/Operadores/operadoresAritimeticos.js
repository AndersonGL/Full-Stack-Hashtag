// Adição 


let valor  = 10;

let segundoValor = 43;

console.log(valor + segundoValor); // Adição

//let mensagem = "20";
//console.log(mensagem + valor); // não é um aritmético.


console.log(segundoValor - valor); // Subtração
console.log(valor * segundoValor); // Multiplicação
console.log(valor / segundoValor); // Divisão

// Modulo

console.log(valor % segundoValor); // Resto da divisão


// Operadores Aritméticos Avançados

//Exponenciação **

let base = 2;
let expoente = 3;
let resultado = base ** expoente; // 2^3 = 8
console.log(resultado); // 8


// Incremento ++ e Decremento (-- diminuir o valor 1)

let controle = 0;
console.log(controle++); // 0 (valor atual) e depois incrementa para 1
console.log(controle); // 1 (valor atualizado) 

console.log(controle--); //Decremento

